    private void updateParamComboBoxSoon( final Map<String,String> dss ) {
        Runnable run= new Runnable() {
            @Override
            public void run() {
                String[] ss= new String[ dss.size() ];
                ss= dss.keySet().toArray( ss );
                //paramComboBox.setModel( new DefaultComboBoxModel(ss) );
                //paramComboBox.setEnabled(true);
                
                if ( ss.length==0 ) {
                    paramList.setModel( getMessageModel("(No plottable parameters)") );
                    return;
                }
                DefaultListModel lm= new DefaultListModel();
                for ( String s: ss ) {
                    lm.addElement(s);
                }
                paramList.setModel( lm );
                
                String lparam= param;
                if ( lparam!=null ) {
                    lparam= lparam.replaceAll("\\+"," ");
                    paramList.setSelectedValue( lparam, true );
                }
            }
        };
        SwingUtilities.invokeLater(run);
    }
    
