    /** Creates new form ViewRow */
    public ViewRow(WalkImageSequence seq) {
        super(seq);
        initComponents();
        viewScrollPane.getHorizontalScrollBar().setUnitIncrement(50); //TODO: set according to thumb size
        sequenceChanged();
        
    }

    @Override
    protected void sequenceChanged() {
        if (seq != null) {
            thumbs.clear();
            thumbPanel.removeAll();
            for (int i = 0; i < seq.size(); i++) {
                PngWalkThumbPanel p = new PngWalkThumbPanel(seq.imageAt(i));
                p.setCaptionString(String.valueOf(i));
                p.setShowCaption(true);
                thumbPanel.add(p);
                thumbs.add(p);
            }
        }
    }

